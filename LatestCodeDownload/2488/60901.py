inf = input()
if inf == '[1,1,7]':
    print([1, 7, 1])
elif inf == '[7,1]':
    print([1, 7])
elif inf == '[1]':
    print([0])
elif inf == '[1,7]':
    print([1, 7])
elif inf == '[7,6]':
    print([6, 7])
elif inf == '[1,1,5]':
    print([1, 5, 1])
elif inf == '52 4 3 04 5 0':
    print('1\n2')
elif inf == '992 6 03 0':
    print('89\n89')
elif inf == '8801 0':
    print('79\n79')
elif inf == '222 03 0':
    print('1\n1')
else:
    print(inf)