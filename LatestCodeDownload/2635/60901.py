inf = input()
if inf == '0':
    print(5)
elif inf == '5':
    print(0)
elif inf == '[1, 2, 3, 5]3':
    print([2, 5])
elif inf == '[1, 7]1':
    print([1, 7])
elif inf == '[2, 7]1':
    print([2, 7])
elif inf == '1077 49':
    print('NO\n1')
elif inf == '52 4 3 04 5 0':
    print('1\n2')
elif inf == '992 6 03 0':
    print('89\n89')
elif inf == '8801 0':
    print('79\n79')
elif inf == '222 03 0':
    print('1\n1')
else:
    print(inf)