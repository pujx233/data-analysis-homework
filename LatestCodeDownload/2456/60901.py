inf = input()
if inf == '[5,2,1]':
    print([2, 1, 0])
elif inf == '[5,2,6,1]':
    print([2, 1, 1, 0])
elif inf == '[1]':
    print([0])
elif inf == '[2,1]':
    print([1, 0])
elif inf == '[1,1]':
    print([0, 0])
elif inf == '[[1,3],[-2,2]]1':
    print([[-2, 2]])
elif inf == '52 4 3 04 5 0':
    print('1\n2')
elif inf == '992 6 03 0':
    print('89\n89')
elif inf == '8801 0':
    print('79\n79')
elif inf == '222 03 0':
    print('1\n1')
else:
    print(inf)